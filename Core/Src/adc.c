void breath_init(void)
{
		HAL_TIM_PWM_Start(&htim2,TIM_CHANNEL_2);
        ///只用了adc的单通道1 获取一次结果
        ///不断dma采集，一组为16位，一共采1*399个16位数据
		HAL_ADC_Start_DMA(&hadc1, (uint32_t*)&adc1_value, ADC1_CHANNEL_COUNT*ADC1_BUFFER_SIZE);
		init_flag=0;
		collect_num=1;
        ///头和尾用作桢头尾部，2*399 = 798
		adc_buffer[0]=0XAB;
		start_adc_flag=1;
		
}

void breath_send(void)
{
	uint32_t bre_temp;
	if(start_adc_flag)
	{
		for(int i=0;i<399;i++)
		{
		adc_buffer[collect_num]=adc1_value[i] & 0X00FF;///取出低八位的数据
		adc_buffer[collect_num+1]=adc1_value[i]>>8;///取出高八位的数据
		collect_num=collect_num+2;
		}
		if((collect_num)==799)//缓存区已满，从1开始就是798个数据了
		{
			adc_buffer[799]=0xBB;
			collect_num=1;//清空缓存区
		    //test500hz_count++;
			vTaskSuspendAll();
            ///将数据块上传，避免被被打断，时序非常重要，因此挂起所有的进程
			bre_temp=M8266WIFI_SPI_Send_BlockData((uint8_t*)adc_buffer,800,1000,0,NULL,0,NULL);
			xTaskResumeAll();				
		}//8k采样
	}
}

